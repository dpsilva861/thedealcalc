function buildExecutiveSummary(result: RedlineResult): Paragraph[] {
  const paragraphs: Paragraph[] = [
    new Paragraph({
      heading: HeadingLevel.HEADING_1,
      spacing: { after: 200 },
      children: [
        new TextRun({ text: "Executive Summary", bold: true, color: "1E3A5F" }),
      ],
    }),
  ];

  // Key concerns
  if (result.summary.key_concerns.length > 0) {
    paragraphs.push(
      new Paragraph({
        heading: HeadingLevel.HEADING_2,
        spacing: { before: 200, after: 100 },
        children: [new TextRun({ text: "Key Concerns", bold: true, color: "CC0000" })],
      })
    );
    for (const concern of result.summary.key_concerns) {
      paragraphs.push(
        new Paragraph({
          bullet: { level: 0 },
          children: [new TextRun({ text: concern, size: 22 })],
        })
      );
    }
  }

  // Strengths
  if (result.summary.strengths.length > 0) {
    paragraphs.push(
      new Paragraph({
        heading: HeadingLevel.HEADING_2,
        spacing: { before: 200, after: 100 },
        children: [new TextRun({ text: "Strengths", bold: true, color: "228B22" })],
      })
    );
    for (const strength of result.summary.strengths) {
      paragraphs.push(
        new Paragraph({
          bullet: { level: 0 },
          children: [new TextRun({ text: strength, size: 22 })],
        })
      );
    }
  }

  // Stats
  const criticalCount = result.redlines.filter((r) => r.severity === "critical").length;
  const majorCount = result.redlines.filter((r) => r.severity === "major").length;
  const minorCount = result.redlines.filter((r) => r.severity === "minor").length;

  paragraphs.push(
    new Paragraph({
      spacing: { before: 200, after: 100 },
      children: [
        new TextRun({ text: "Issues Found: ", bold: true, size: 22 }),
        new TextRun({ text: `${criticalCount} Critical`, color: "FF0000", size: 22 }),
        new TextRun({ text: ` | ${majorCount} Major`, color: "FF8C00", size: 22 }),
        new TextRun({ text: ` | ${minorCount} Minor`, color: "DAA520", size: 22 }),
        new TextRun({ text: ` | ${result.missing_provisions.length} Missing Provisions`, color: "4169E1", size: 22 }),
      ],
    })
  );

  return paragraphs;
}

function buildRedlineSection(redlines: RedlineItem[]): Paragraph[] {
  if (redlines.length === 0) return [];

  const paragraphs: Paragraph[] = [
    new Paragraph({
      heading: HeadingLevel.HEADING_1,
      spacing: { before: 400, after: 200 },
      children: [
        new TextRun({ text: "Redline Items", bold: true, color: "1E3A5F" }),
      ],
    }),
  ];

  // Sort by severity
  const sorted = [...redlines].sort((a, b) => {
    const order = { critical: 0, major: 1, minor: 2, informational: 3 };
    return (order[a.severity] ?? 4) - (order[b.severity] ?? 4);
  });

  for (const item of sorted) {
    const severityColor = SEVERITY_COLORS[item.severity] || "000000";
    const bgColor = SEVERITY_BG[item.severity] || "FFFFFF";

    paragraphs.push(
      new Paragraph({
        spacing: { before: 300, after: 50 },
        shading: { type: ShadingType.CLEAR, color: "auto", fill: bgColor },
        border: {
          left: { style: BorderStyle.SINGLE, size: 6, color: severityColor },
        },
        children: [
          new TextRun({
            text: `[${item.severity.toUpperCase()}] `,
            bold: true,
            color: severityColor,
            size: 22,
          }),
          new TextRun({
            text: `${item.section} - ${item.category}`,
            bold: true,
            size: 22,
          }),
        ],
      }),
      new Paragraph({
        spacing: { after: 50 },
        children: [
          new TextRun({ text: "Issue: ", bold: true, size: 20 }),
          new TextRun({ text: item.issue, size: 20 }),
        ],
      }),
      new Paragraph({
        spacing: { after: 50 },
        children: [
          new TextRun({ text: "Original: ", bold: true, size: 20, color: "888888" }),
          new TextRun({ text: `"${item.original_text}"`, italics: true, size: 20, color: "888888" }),
        ],
      }),
      new Paragraph({
        spacing: { after: 50 },
        children: [
          new TextRun({ text: "Recommendation: ", bold: true, size: 20 }),
          new TextRun({ text: item.recommendation, size: 20 }),
        ],
      }),
      new Paragraph({
        spacing: { after: 50 },
        children: [
          new TextRun({ text: "Suggested Language: ", bold: true, size: 20, color: "228B22" }),
          new TextRun({ text: `"${item.suggested_language}"`, italics: true, size: 20, color: "228B22" }),
        ],
      }),
      new Paragraph({
        spacing: { after: 100 },
        children: [
          new TextRun({ text: "Market Benchmark: ", bold: true, size: 18, color: "666666" }),
          new TextRun({ text: item.market_benchmark, size: 18, color: "666666" }),
        ],
      })
    );
  }

  return paragraphs;
}

function buildMissingProvisionsSection(
  provisions: RedlineResult["missing_provisions"]
): Paragraph[] {
  if (provisions.length === 0) return [];

  const paragraphs: Paragraph[] = [
    new Paragraph({
      heading: HeadingLevel.HEADING_1,
      spacing: { before: 400, after: 200 },
      children: [
        new TextRun({ text: "Missing Provisions", bold: true, color: "1E3A5F" }),
      ],
    }),
  ];

  for (const provision of provisions) {
    const importanceColor =
      provision.importance === "critical" ? "FF0000" : provision.importance === "recommended" ? "FF8C00" : "4169E1";

    paragraphs.push(
      new Paragraph({
        spacing: { before: 200, after: 50 },
        children: [
          new TextRun({
            text: `[${provision.importance.toUpperCase()}] `,
            bold: true,
            color: importanceColor,
            size: 22,
          }),
          new TextRun({ text: provision.provision, bold: true, size: 22 }),
        ],
      }),
      new Paragraph({
        spacing: { after: 50 },
        children: [
          new TextRun({ text: "Rationale: ", bold: true, size: 20 }),
          new TextRun({ text: provision.rationale, size: 20 }),
        ],
      }),
      new Paragraph({
        spacing: { after: 100 },
        children: [
          new TextRun({ text: "Suggested Language: ", bold: true, size: 20, color: "228B22" }),
          new TextRun({ text: `"${provision.suggested_language}"`, italics: true, size: 20, color: "228B22" }),
        ],
      })
    );
  }

  return paragraphs;
}

function buildNegotiationStrategy(
  strategy: RedlineResult["negotiation_strategy"]
): Paragraph[] {
  return [
    new Paragraph({
      heading: HeadingLevel.HEADING_1,
      spacing: { before: 400, after: 200 },
      children: [
        new TextRun({ text: "Negotiation Strategy", bold: true, color: "1E3A5F" }),
      ],
    }),
    new Paragraph({
      spacing: { after: 100 },
      children: [
        new TextRun({ text: "Overall Approach: ", bold: true, size: 22 }),
        new TextRun({ text: strategy.overall_approach, size: 22 }),
      ],
    }),
    new Paragraph({
      spacing: { after: 100 },
      children: [
        new TextRun({ text: "Opening Position: ", bold: true, size: 22 }),
        new TextRun({ text: strategy.opening_position, size: 22 }),
      ],
    }),
    new Paragraph({
      heading: HeadingLevel.HEADING_2,
      spacing: { before: 200, after: 100 },
      children: [new TextRun({ text: "Hard Lines (Do Not Concede)", bold: true, color: "CC0000" })],
    }),
    ...strategy.hard_lines.map(
      (line) =>
        new Paragraph({
          bullet: { level: 0 },
          children: [new TextRun({ text: line, size: 22 })],
        })
    ),
    new Paragraph({
      heading: HeadingLevel.HEADING_2,
      spacing: { before: 200, after: 100 },
      children: [new TextRun({ text: "Concession Priorities (Give First If Needed)", bold: true, color: "228B22" })],
    }),
    ...strategy.concession_priorities.map(
      (item) =>
        new Paragraph({
          bullet: { level: 0 },
          children: [new TextRun({ text: item, size: 22 })],
        })
    ),
  ];
}

