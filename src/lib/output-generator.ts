import type { RedlineResult, RedlineItem } from "@/types";

const SEVERITY_COLORS: Record<string, string> = {
  critical: "FF0000",
  major: "FF8C00",
  minor: "FFD700",
  informational: "4169E1",
};

const SEVERITY_BG: Record<string, string> = {
  critical: "FFE0E0",
  major: "FFF0E0",
  minor: "FFFFF0",
  informational: "E8F0FE",
};

/**
 * Generate a professional redline DOCX report.
 */
export async function generateRedlinedDocx(
  result: RedlineResult,
  originalText: string
): Promise<Buffer> {
  const doc = new Document({
    creator: "RedlineIQ",
    title: "RedlineIQ LOI Analysis Report",
    description: "AI-Powered LOI Redline Analysis",
    sections: [
      {
        properties: {},
        children: [
          // Legal disclaimer
          new Paragraph({
            alignment: AlignmentType.CENTER,
            spacing: { after: 400 },
            border: {
              top: { style: BorderStyle.SINGLE, size: 1, color: "CCCCCC" },
              bottom: { style: BorderStyle.SINGLE, size: 1, color: "CCCCCC" },
              left: { style: BorderStyle.SINGLE, size: 1, color: "CCCCCC" },
              right: { style: BorderStyle.SINGLE, size: 1, color: "CCCCCC" },
            },
            shading: { type: ShadingType.CLEAR, color: "auto", fill: "FFF9E6" },
            children: [
              new TextRun({
                text: "DISCLAIMER: ",
                bold: true,
                color: "B8860B",
                size: 18,
              }),
              new TextRun({
                text: "RedlineIQ is not a law firm and does not provide legal advice. All analysis, deal scores, and recommendations are automated suggestions based on commercial real estate industry benchmarks. Users should consult qualified legal and real estate professionals before acting on any findings. Users are solely responsible for their decisions regarding any LOI or lease transaction.",
                color: "666666",
                size: 18,
              }),
            ],
          }),
          // Title page
          ...buildTitlePage(result),
          // Executive summary
          ...buildExecutiveSummary(result),
          // Redline items
          ...buildRedlineSection(result.redlines),
          // Missing provisions
          ...buildMissingProvisionsSection(result.missing_provisions),
          // Negotiation strategy
          ...buildNegotiationStrategy(result.negotiation_strategy),
          // Footer
          new Paragraph({
            alignment: AlignmentType.CENTER,
            spacing: { before: 600 },
            children: [
              new TextRun({
                text: "Generated by RedlineIQ - AI-Powered CRE LOI Analysis",
                italics: true,
                color: "888888",
                size: 18,
              }),
            ],
          }),
        ],
      },
    ],
  });

  const buffer = await Packer.toBuffer(doc);
  return Buffer.from(buffer);
}

